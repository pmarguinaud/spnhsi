LOGICAL :: LPRESSURE, LPRESSURE_MON1, LPRESSURE_MON2, LSCALE_HEIGHT_MON, LVAR_NAME
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP4 (:,:,:,:)
READ (KLUN) LCOLUMN_MASS_MON
IF (LCOLUMN_MASS_MON) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%COLUMN_MASS_MON (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%COLUMN_MASS_MON,1):UBOUND(SELF%COLUMN_MASS_MON,1), LBOUND(SELF%COLUMN_MASS_MON,2):UBOUND(SELF%COLUMN_MASS_MON,2), LBOUND(SELF%COLUMN_MASS_MON,3):UBOUND(SELF%COLUMN_MASS_MON,3), LBOUND(SELF%COLUMN_MASS_MON,4):UBOUND(SELF%COLUMN_MASS_MON,4)))
  READ (KLUN) ZTMP4
  SELF%COLUMN_MASS_MON = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) LSCALE_HEIGHT_MON
IF (LSCALE_HEIGHT_MON) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%SCALE_HEIGHT_MON (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%SCALE_HEIGHT_MON,1):UBOUND(SELF%SCALE_HEIGHT_MON,1), LBOUND(SELF%SCALE_HEIGHT_MON,2):UBOUND(SELF%SCALE_HEIGHT_MON,2)))
  READ (KLUN) ZTMP2
  SELF%SCALE_HEIGHT_MON = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LCOLUMN_MASS
IF (LCOLUMN_MASS) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%COLUMN_MASS (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(SELF%COLUMN_MASS,1):UBOUND(SELF%COLUMN_MASS,1), LBOUND(SELF%COLUMN_MASS,2):UBOUND(SELF%COLUMN_MASS,2), LBOUND(SELF%COLUMN_MASS,3):UBOUND(SELF%COLUMN_MASS,3)))
  READ (KLUN) ZTMP3
