IF (LAER_TYPE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%AER_TYPE (IL1(1):IU1(1)))
  READ (KLUN) SELF%AER_TYPE
ENDIF
READ (KLUN) LMMR_MON1
IF (LMMR_MON1) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (SELF%MMR_MON1 (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%MMR_MON1,1):UBOUND(SELF%MMR_MON1,1), LBOUND(SELF%MMR_MON1,2):UBOUND(SELF%MMR_MON1,2), LBOUND(SELF%MMR_MON1,3):UBOUND(SELF%MMR_MON1,3), LBOUND(SELF%MMR_MON1,4):UBOUND(SELF%MMR_MON1,4)))
  READ (KLUN) ZTMP4
