  ALLOCATE (SELF%MMR_MON2 (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(SELF%MMR_MON2,1):UBOUND(SELF%MMR_MON2,1), LBOUND(SELF%MMR_MON2,2):UBOUND(SELF%MMR_MON2,2), LBOUND(SELF%MMR_MON2,3):UBOUND(SELF%MMR_MON2,3), LBOUND(SELF%MMR_MON2,4):UBOUND(SELF%MMR_MON2,4)))
