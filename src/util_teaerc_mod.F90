ENDIF
READ (KLUN) LCUM_FRACTION
IF (LCUM_FRACTION) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%CUM_FRACTION (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(SELF%CUM_FRACTION,1):UBOUND(SELF%CUM_FRACTION,1), LBOUND(SELF%CUM_FRACTION,2):UBOUND(SELF%CUM_FRACTION,2)))
