SUBROUTINE SAVE_TEMMP (SELF, KLUN)

IMPLICIT NONE
TYPE (TEMMP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LMYENS, LNEALLNS, LNEPROCN, LNEPTRNS, LNUEMPP
LNEPROCN = ASSOCIATED (SELF%NEPROCN)
WRITE (KLUN) LNEPROCN
IF (LNEPROCN) THEN
  WRITE (KLUN) LBOUND (SELF%NEPROCN)
  WRITE (KLUN) UBOUND (SELF%NEPROCN)
  WRITE (KLUN) SELF%NEPROCN
ENDIF
WRITE (KLUN) SELF%NUEMP
LMYENS = ASSOCIATED (SELF%MYENS)
WRITE (KLUN) LMYENS
IF (LMYENS) THEN
  WRITE (KLUN) LBOUND (SELF%MYENS)
  WRITE (KLUN) UBOUND (SELF%MYENS)
  WRITE (KLUN) SELF%MYENS
ENDIF
LNUEMPP = ASSOCIATED (SELF%NUEMPP)
WRITE (KLUN) LNUEMPP
IF (LNUEMPP) THEN
  WRITE (KLUN) LBOUND (SELF%NUEMPP)
  WRITE (KLUN) UBOUND (SELF%NUEMPP)
  WRITE (KLUN) SELF%NUEMPP
ENDIF
LNEALLNS = ASSOCIATED (SELF%NEALLNS)
WRITE (KLUN) LNEALLNS
IF (LNEALLNS) THEN
  WRITE (KLUN) LBOUND (SELF%NEALLNS)
  WRITE (KLUN) UBOUND (SELF%NEALLNS)
  WRITE (KLUN) SELF%NEALLNS
ENDIF
LNEPTRNS = ASSOCIATED (SELF%NEPTRNS)
WRITE (KLUN) LNEPTRNS
IF (LNEPTRNS) THEN
  WRITE (KLUN) LBOUND (SELF%NEPTRNS)
  WRITE (KLUN) UBOUND (SELF%NEPTRNS)
  WRITE (KLUN) SELF%NEPTRNS
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TEMMP (SELF, KLUN)

IMPLICIT NONE
TYPE (TEMMP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LMYENS, LNEALLNS, LNEPROCN, LNEPTRNS, LNUEMPP
READ (KLUN) LNEPROCN
IF (LNEPROCN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NEPROCN (IL1(1):IU1(1)))
  READ (KLUN) SELF%NEPROCN
ELSE
  NULLIFY (SELF%NEPROCN)
ENDIF
READ (KLUN) SELF%NUEMP
READ (KLUN) LMYENS
IF (LMYENS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%MYENS (IL1(1):IU1(1)))
  READ (KLUN) SELF%MYENS
ELSE
  NULLIFY (SELF%MYENS)
ENDIF
READ (KLUN) LNUEMPP
IF (LNUEMPP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NUEMPP (IL1(1):IU1(1)))
  READ (KLUN) SELF%NUEMPP
ELSE
  NULLIFY (SELF%NUEMPP)
ENDIF
READ (KLUN) LNEALLNS
IF (LNEALLNS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NEALLNS (IL1(1):IU1(1)))
  READ (KLUN) SELF%NEALLNS
ELSE
  NULLIFY (SELF%NEALLNS)
ENDIF
READ (KLUN) LNEPTRNS
IF (LNEPTRNS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NEPTRNS (IL1(1):IU1(1)))
  READ (KLUN) SELF%NEPTRNS
ELSE
  NULLIFY (SELF%NEPTRNS)
ENDIF
END SUBROUTINE


